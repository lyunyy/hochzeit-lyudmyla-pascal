let currentLanguage = 'de';

function textFor(key) {
  return translations[currentLanguage][key] ?? translations.de[key] ?? key;
}

function applyLanguage(language) {
  currentLanguage = translations[language] ? language : 'de';
  document.documentElement.lang = currentLanguage;

  document.querySelectorAll('[data-i18n]').forEach((element) => {
    element.textContent = textFor(element.dataset.i18n);
  });

  document.querySelectorAll('[data-i18n-html]').forEach((element) => {
    element.innerHTML = textFor(element.dataset.i18nHtml);
  });

  document.querySelectorAll('[data-i18n-alt]').forEach((element) => {
    element.alt = textFor(element.dataset.i18nAlt);
  });

  document.querySelectorAll('[data-i18n-aria-label]').forEach((element) => {
    element.setAttribute('aria-label', textFor(element.dataset.i18nAriaLabel));
  });

  document.querySelectorAll('.language-button').forEach((button) => {
    const isActive = button.dataset.language === currentLanguage;
    button.classList.toggle('is-active', isActive);
    button.setAttribute('aria-pressed', String(isActive));
  });

  document.title = textFor('pageTitle');
  const description = document.querySelector('meta[name="description"]');
  const ogTitle = document.querySelector('meta[property="og:title"]');
  const ogDescription = document.querySelector('meta[property="og:description"]');
  if (description) description.content = textFor('pageDescription');
  if (ogTitle) ogTitle.content = textFor('pageTitle');
  if (ogDescription) ogDescription.content = textFor('pageDescription');

  try {
    localStorage.setItem('wedding-language', currentLanguage);
  } catch (_) {
    // Die Website funktioniert auch ohne Local Storage.
  }

  updateCountdown();
}

document.querySelectorAll('.language-button').forEach((button) => {
  button.addEventListener('click', () => applyLanguage(button.dataset.language));
});

const weddingDate = new Date('2026-08-20T10:00:00+02:00').getTime();
const countdownFields = {
  days: document.getElementById('days'),
  hours: document.getElementById('hours'),
  minutes: document.getElementById('minutes'),
  seconds: document.getElementById('seconds')
};
const countdownCaption = document.getElementById('countdown-caption');

function updateCountdown() {
  const difference = weddingDate - Date.now();
  const remaining = Math.max(0, difference);
  const days = Math.floor(remaining / 86400000);
  const hours = Math.floor(remaining / 3600000) % 24;
  const minutes = Math.floor(remaining / 60000) % 60;
  const seconds = Math.floor(remaining / 1000) % 60;

  countdownFields.days.textContent = String(days);
  countdownFields.hours.textContent = String(hours).padStart(2, '0');
  countdownFields.minutes.textContent = String(minutes).padStart(2, '0');
  countdownFields.seconds.textContent = String(seconds).padStart(2, '0');
  countdownCaption.textContent = difference <= 0 ? textFor('weddingToday') : textFor('countdownCaption');
}

updateCountdown();
setInterval(updateCountdown, 1000);

const proposalVideo = document.getElementById('proposal-video');
const playButton = document.getElementById('video-play');

playButton.addEventListener('click', async () => {
  proposalVideo.muted = false;
  proposalVideo.volume = 1;
  try {
    await proposalVideo.play();
    playButton.classList.add('is-hidden');
  } catch (_) {
    proposalVideo.controls = true;
  }
});

proposalVideo.addEventListener('play', () => playButton.classList.add('is-hidden'));
proposalVideo.addEventListener('ended', () => playButton.classList.remove('is-hidden'));

